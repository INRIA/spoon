package spoon.visualisation.spoon;

/**
 * An immutable class that represents a structure of four strings.
 */
public class Tuple4 {
	public final String v1;
	public final String v2;
	public final String v3;
	public final String v4;

	public Tuple4(final String v1, final String v2, final String v3, final String v4) {
		super();
		this.v1 = v1;
		this.v2 = v2;
		this.v3 = v3;
		this.v4 = v4;
	}
}
