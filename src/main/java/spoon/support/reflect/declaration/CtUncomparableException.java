package spoon.support.reflect.declaration;

public class CtUncomparableException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public CtUncomparableException() {
		super();
	}

	public CtUncomparableException(String message, Throwable cause) {
		super(message, cause);
	}

	public CtUncomparableException(String message) {
		super(message);
	}

	public CtUncomparableException(Throwable cause) {
		super(cause);
	}

}
